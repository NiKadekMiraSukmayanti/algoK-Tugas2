# Program untuk mengkonversi satuan dari meter ke sentimeter
# Langkah 1: Input
# Meminta input dari pengguna untuk panjang dalam satuan meter
meter = int(input("Masukkan panjang dalam meter: "))

# Langkah 2: Proses (Konversi)
# 1 meter = 100 cm
sentimeter = meter * 100

# Langkah 3: proses
# Menampilkan hasil konversi ke layar
txhasil=(f"{meter} meter = {sentimeter} sentimeter")

#mencetak hasil : output
print(txhasil)
